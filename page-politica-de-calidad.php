			<h2>Gestión integrada</h2>
<p>Integrar la calidad, seguridad y salud en la gestión del trabajo por parte de la línea de mando.
Los equipos de trabajo serán responsables de la aplicación del sistema de gestión y de la
obtención de sus resultados. </p>
<h2>Cumplimiento de las normas</h2>
<p>Cumplir los requisitos legales vigentes que se elabora considerando las exigencias legislativas,
así como con otros compromisos que la organización suscriba, planificando en consecuencia.</p>
<h2>Mejora continua</h2>
<p>Establecer objetivos y metas de mejora, teniendo en cuenta los requerimientos de los clientes y
la legislación, evaluar el desempeño de forma continua, aplicando las correcciones necesarias
para alcanzar los logros propuestos, definiendo procesos de verificación, auditoría y control
para asegurarlos.  </p>
<p>Sea cual sea el puesto, todos los empleados son responsables de su propia seguridad, así
como de contribuir a la calidad, seguridad y salud.
Si durante la realización de los trabajos existiera cualquier situación que afecte a la seguridad
ante la realización del trabajo, todos los empleados tienen la responsabilidad de elegir la
seguridad, apoyando la dirección siempre esta elección.
Agriverdes del Mediterráneo considera que cumplir con esta política es responsabilidad de
todos sus empleados. </p>

